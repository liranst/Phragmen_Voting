from Protocol1 import protocol_1_share
from Protocol2 import protocol_2_reconstruct


def protocol_3_1_affine_combination(alpha, beta, gamma, shares_u, shares_v, p):
    """
    Input:
        alpha, beta, gamma - מספרים קבועים (ציבוריים) הידועים לכולם
        shares_u - רשימת המניות (x, y) של הסוד הראשון (u)
        shares_v - רשימת המניות (x, y) של הסוד השני (v)
        p - המספר הראשוני שמגדיר את השדה

    Output: רשימה חדשה של מניות, המייצגת את הסוד החדש: w = alpha + beta*u + gamma*v
    """
    # כאן נשמור את המניות החדשות שניצור
    w_shares = []

    # ---------------------------------------------------------
    # שלב 1: עוברים על המניות של כל משתתף במקביל
    # אנו מניחים ששתי הרשימות מסודרות באותו אופן לפי ה-x (מזהה המשתתף)
    # ---------------------------------------------------------
    for i in range(len(shares_u)):
        # מחלצים את מזהה המשתתף (x) והמניה (y) עבור הסוד u
        x_u, y_u = shares_u[i]
        # print(f"x_u, y_u = {x_u, y_u}")
        # מחלצים את מזהה המשתתף (x) והמניה (y) עבור הסוד v
        x_v, y_v = shares_v[i]
        # print(f"x_v, y_v = {x_v, y_v}")

        # ---------------------------------------------------------
        # שלב 2: בדיקת תקינות (אופציונלי אך מומלץ)
        # מוודאים שאנחנו אכן מחברים מניות של אותו משתתף
        # ---------------------------------------------------------
        if x_u != x_v:
            raise ValueError(f"אי התאמה במזהי המשתתפים: {x_u} לא תואם ל-{x_v}")

        # ---------------------------------------------------------
        # שלב 3: הפעלת הנוסחה המקומית
        # החישוב הוא: y_new = alpha + beta * y_u + gamma * y_v
        # וכמובן, הכל מתבצע מודולו p כדי להישאר בשדה הסופי!
        # ---------------------------------------------------------
        y_new = (alpha + (beta * y_u) + (gamma * y_v)) % p

        # ---------------------------------------------------------
        # שלב 4: הוספת המניה החדשה לרשימה
        # מזהה המשתתף נשאר זהה (x_u), אך כעת יש לו ערך y חדש
        # ---------------------------------------------------------
        print(f"x_u, y_new = {x_u, y_new}")
        w_shares.append((x_u, y_new))

    # מחזירים את רשימת המניות החדשה
    return w_shares


