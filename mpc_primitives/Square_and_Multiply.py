# Square_and_Multiply
def square_and_multiply(x, c, n):
    """
    מחשב את הערך של x בחזקת c מודולו n.

    פרמטרים:
    x - הבסיס
    c - המעריך (אקספוננט)
    n - המודולוס (השדה)
    """
    # המרת המעריך למחרוזת בינארית (למשל 13 הופך ל-'1101')
    binary_c = bin(c)[2:]
    l = len(binary_c)

    # אתחול המשתנה z ל-1 (שורה 1 באלגוריתם)
    z = 1

    # לולאה מ-0 עד l-1 (שורה 2 באלגוריתם)
    for i in range(l):
        # העלאה בריבוע מודולרית (שורה 3 באלגוריתם)
        z = (z ** 2) % n

        # בדיקה האם הביט הנוכחי (מהסוף להתחלה לפי האינדקס l-1-i) הוא 1
        # הערה: binary_c[i] ניגש לביטים מה-MSB ל-LSB
        if binary_c[i] == '1':
            # כפל בבסיס x (שורה 5 באלגוריתם)
            z = (z * x) % n

    return z

