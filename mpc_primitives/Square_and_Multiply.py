    # דוגמה לשימוש: חשב 3 בחזקת 13 מודולו 11
    # 3^13 = 1,594,323.
    # 1,594,323 mod 11 = 3
    result = square_and_multiply(3, 13, 11)
    print(f"Result: {result}")
