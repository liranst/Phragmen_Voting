def binary_long_division(u, v):
    """
    פונקציה המממשת חילוק ארוך בינארי (אלגוריתם 12).
    u - המונה (המספר שאותו מחלקים)
    v - המחלק
    """

    # שלב הכנה: המרת המספר u לייצוג בינארי (רשימה של אפסים ואחדים)
    # למשל, עבור 13 נקבל את הרשימה [1, 1, 0, 1]
    u_bits = [int(bit) for bit in format(u, 'b')]
    s = len(u_bits)  # כמות הספרות (הביטים)

    # שורות 1-2 באלגוריתם: אתחול המנה (q) והשארית (r) לאפס
    q = 0
    r = 0

    print(f"מתחילים תהליך חילוק ארוך של {u} ב-{v}")
    print(f"הייצוג הבינארי של המונה ({u}) הוא: {u_bits}")
    print("=" * 40 + "\n")

    # שורה 3 באלגוריתם: רצים על הספרות משמאל לימין
    # אנו משתמשים ב-enumerate כדי לעקוב גם אחרי מספר הסיבוב
    for j, u_j in enumerate(u_bits):
        print(f"--- סיבוב {j + 1} (עבור הספרה u_j = {u_j}) ---")

        # שורה 4: עדכון השארית הזמנית (מכפילים ב-2 ומוסיפים את הספרה הנוכחית)
        r = 2 * r + u_j
        print(f"מעדכנים שארית: r = 2*r + u_j  =>  r = {r}")

        # שורה 5: האם השארית קטנה מהמחלק?
        if r < v:
            print(f"האם r < v? (האם {r} < {v}?) -> כן!")
            # שורה 6: הספרה שנוספת לתוצאה היא 0
            q_j = 0
            print(f"לכן מוסיפים לתוצאה את הספרה: q_j = 0")

        # שורה 7: אחרת (השארית גדולה או שווה למחלק)
        else:
            print(f"האם r < v? (האם {r} < {v}?) -> לא!")
            # שורה 8: הספרה שנוספת לתוצאה היא 1
            q_j = 1
            print(f"לכן מוסיפים לתוצאה את הספרה: q_j = 1")

            # שורה 9: מחסרים את המחלק מהשארית
            r = r - v
            print(f"מחסרים את המחלק מהשארית: r = r - v  =>  r = {r}")

        # שורה 10: מעדכנים את המנה הכללית (מזיזים שמאלה על ידי כפל ב-2 ומוסיפים את הספרה)
        q = 2 * q + q_j
        print(f"מעדכנים את המנה הכללית: q = 2*q + q_j  =>  q = {q}\n")

    # סיום הלולאה, מחזירים את התוצאות
    return q, r


# ==========================================
# הרצת הדוגמה שלנו: 13 לחלק ל-3
# ==========================================
if __name__ == "__main__":
    u = 13
    v = 3

    quotient, remainder = binary_long_division(u, v)

    print("=" * 40)
    print(f"תוצאה סופית: המנה היא {quotient}, והשארית היא {remainder}")
    print("=" * 40)