# protocol_3
from MPC_over_secret_shares.Protocol1 import protocol_1_share
import random


def protocol_3_rng(n, t, p, print_ = False):
    """
    מייצר מניות של מספר אקראי סודי שאיש אינו יודע מהו.
    n - מספר משתתפים
    t - סף שחזור
    p - השדה הראשוני
    """
    # כאן נשמור את כל המניות שכל המשתתפים יצרו וחילקו
    all_generated_shares = []

    # ---------------------------------------------------------
    # שורה 1: forall i in [n] do
    # כל משתתף P_i (מ-1 עד n) מבצע את הפעולות הבאות:
    # ---------------------------------------------------------
    for i in range(1, n + 1):
        # ---------------------------------------------------------
        # שורה 2: P_i generates a random r_i in Z_p
        # המשתתף מגריל מספר אקראי סודי משלו
        # ---------------------------------------------------------
        r_i = random.randint(0, p - 1)
        print(f"r_i = {r_i}")
        # ---------------------------------------------------------
        # שורה 3: P_i runs Share(r_i, i; t)
        # המשתתף מפעיל את פרוטוקול 1 כדי לפצל את המספר האקראי שלו למניות.
        # (אנו מניחים שהפונקציה protocol_1_share זמינה לנו בקוד)
        # ---------------------------------------------------------
        shares_from_i = protocol_1_share(r_i, n, t, p)

        # שומרים את רשימת המניות שהמשתתף ה-i יצר עבור כולם
        all_generated_shares.append(shares_from_i)
    # כעת, לכל משתתף יש רשימה של מניות שהוא יצר.
    # נכין רשימה חדשה שתכיל את המניות הסופיות והמסוכמות עבור כל משתתף.
    final_random_shares = []

    # ---------------------------------------------------------
    # שורה 4: forall j in [n] do
    # כל משתתף P_j (המקבל) אוסף את כל חלקי המידע שנשלחו אליו:
    # ---------------------------------------------------------
    for j in range(1, n + 1):
        sum_y = 0  # משתנה לצבירת סכום המניות שהמשתתף קיבל
        print(f"P{j}: [[r]]{j} = Σ[[r]]{j} =", end="")
        # עוברים על כל רשימות המניות שנוצרו על ידי המשתתפים האחרים (P_i)
        for shares_from_i in all_generated_shares:
            # המניה ששייכת למשתתף j נמצאת במקום ה-(j-1) ברשימה
            x_val, y_val = shares_from_i[j - 1]
            print(f" + {y_val}", end="")
            sum_y += y_val
            # ---------------------------------------------------------
            # שורה 5: P_j sets [[r]]_j = sum_{i=1}^{n} [[r_i]]_j
            # המשתתף j סוכם את כל ערכי ה-Y שהוא קיבל, מודולו p
            # ---------------------------------------------------------

            sum_y = (sum_y + y_val) % p
        print(f" = {sum_y}")

        # מוסיפים את המניה המסוכמת של משתתף j לרשימה הסופית
        final_random_shares.append((j, sum_y))

    # ---------------------------------------------------------
    # Output: A (t,n)-sharing of a secret random r
    # מחזירים את המניות הסופיות. אם משתתפים יחברו יחד ויפעילו
    # את פרוטוקול השחזור על המניות הללו, הם יגלו מספר אקראי חדש לגמרי.
    # ---------------------------------------------------------
    return final_random_shares

