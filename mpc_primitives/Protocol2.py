# Protocol2
def protocol_2_reconstruct(shares, p, print_=False):
    """
    Input: shares - רשימה של מניות (כל מניה היא צמד של x ו-y)
           p - המספר הראשוני שמגדיר את השדה
    Output: הסוד המקורי S (שהוא בעצם ערך הפולינום בנקודה x=0)
    """
    secret = 0  # כאן נצבור את התוצאה הסופית

    # ---------------------------------------------------------
    # שלב 1: עוברים על כל מניה (נקודה) שקיבלנו מהמשתתפים
    # ---------------------------------------------------------
    for i in range(len(shares)):
        # מחלצים את ה-x וה-y של המניה הנוכחית
        x_i, y_i = shares[i]

        # נתחיל לחשב את "משקל לגראנז'" של הנקודה הזו.
        # המשקל מורכב ממונה (Numerator) ומכנה (Denominator).
        numerator = 1
        denominator = 1

        # ---------------------------------------------------------
        # שלב 2: בניית המונה והמכנה בעזרת שאר הנקודות
        # ---------------------------------------------------------
        for j in range(len(shares)):
            # אנחנו לא משווים את הנקודה לעצמה
            if i != j:
                x_j, y_j = shares[j]

                # חישוב המונה: מכפילים ב- (0 פחות ה-x של הנקודה האחרת)
                # שזה בעצם פשוט מינוס x_j
                numerator = (numerator * (-x_j)) % p

                # חישוב המכנה: מכפילים בהפרש בין ה-x שלנו ל-x של הנקודה האחרת
                denominator = (denominator * (x_i - x_j)) % p

        # ---------------------------------------------------------
        # שלב 3: חילוק מודולרי (הכפלה בהופכי)
        # מכיוון שאנחנו בשדה סופי, אי אפשר פשוט "לחלק" במונה/מכנה.
        # במקום זאת, מוצאים את "ההופכי הכפלי" של המכנה בעזרת חזקה (p-2).
        # ---------------------------------------------------------
        denominator_inv = pow(denominator, p - 2, p)

        # עכשיו אפשר להכפיל את המונה בהופכי של המכנה כדי לקבל את המשקל
        lagrange_weight = (numerator * denominator_inv) % p
        if print_:
            print(f"L{i+1}(0) = [{numerator}*{-x_j}]/[{denominator}*({x_i}-{x_j})] = {lagrange_weight}")

        # ---------------------------------------------------------
        # שלב 4: מוסיפים את התרומה של המניה הנוכחית לסוד הכללי
        # מכפילים את ה-Y של המניה במשקל שחישבנו
        # ---------------------------------------------------------
        term = (y_i * lagrange_weight) % p

        secret = (secret + term) % p

    # מחזירים את הסוד השלם!
    return secret


